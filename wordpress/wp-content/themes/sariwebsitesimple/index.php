		<p style="max-width:450px">‘Sari-Sari’ is a word that means ‘many things’ or ‘many dishes in one plate’. In the Philippines, it takes the form of a self-made mini grocery store of everything, eatery and meeting place. It is usually built right in one’s house in a part facing the street. Benches are placed in front so the customers keep the shop owner company, in the process of buying, eating or drinking something, or simply hanging out to have conversations. By day it is a store (tindahan) and lunch place (turo-turo). By night it is a huntahan (meeting place), where people gather to sing, talk, eat and drink the night away. It is a multi-purpose place that can extend its possibilities according to the needs of its community so barber cuts, karaoke sessions, t.v. screenings, can all be part of the repertoire.</p>

